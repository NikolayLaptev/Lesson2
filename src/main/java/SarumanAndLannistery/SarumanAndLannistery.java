package SarumanAndLannistery;

public class SarumanAndLannistery {
    public static void main(String[] args) {
        int infantrymen = 254; // количество пехотинцев у Саруман
        int orcs = 452; // количество орков у Ланнистров
        int infantry = infantrymen + orcs;

        System.out.println("Количество пехотинцев у Саруман " + infantrymen);
        System.out.println("Количество орков у Ланнистров " + orcs);
        System.out.println("Общее количество пехоты у Саруман и Ланнистров " + infantry);
    }
}
